package com.brm.service.portal.bean.update;

public class CcInfo {
	private String address;
	private String city;
	private String country;
	private String debitExp;
	private String debitNum;
	private String debitToken; 
	private String name;
	private String securityId;
	private String state;
	private String zip;
	
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getDebitExp() {
		return debitExp;
	}
	public void setDebitExp(String debitExp) {
		this.debitExp = debitExp;
	}
	public String getDebitNum() {
		return debitNum;
	}
	public void setDebitNum(String debitNum) {
		this.debitNum = debitNum;
	}
	public String getDebitToken() {
		return debitToken;
	}
	public void setDebitToken(String debitToken) {
		this.debitToken = debitToken;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSecurityId() {
		return securityId;
	}
	public void setSecurityId(String securityId) {
		this.securityId = securityId;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
}
