package com.brm.service.portal.bean.update;

public class InvInfo {
	private String address;
	private String city;
	private String country;
	private String deliveryDescr;
	private String deliveryPrefer;
	private String invInstr;
	private String inv_terms;
	private String emailAddr;
	private String name;
	private String state;
	private String zip;
	
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getDeliveryDescr() {
		return deliveryDescr;
	}
	public void setDeliveryDescr(String deliveryDescr) {
		this.deliveryDescr = deliveryDescr;
	}
	public String getDeliveryPrefer() {
		return deliveryPrefer;
	}
	public void setDeliveryPrefer(String deliveryPrefer) {
		this.deliveryPrefer = deliveryPrefer;
	}
	public String getInvInstr() {
		return invInstr;
	}
	public void setInvInstr(String invInstr) {
		this.invInstr = invInstr;
	}
	public String getInv_terms() {
		return inv_terms;
	}
	public void setInv_terms(String inv_terms) {
		this.inv_terms = inv_terms;
	}
	public String getEmailAddr() {
		return emailAddr;
	}
	public void setEmailAddr(String emailAddr) {
		this.emailAddr = emailAddr;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
}
